package org.thshsh.crypt.cryptocompare;

@SuppressWarnings("serial")
public class CryptoCompareException extends RuntimeException {

	public CryptoCompareException() {
		super();
	}

	public CryptoCompareException(String message, Throwable cause) {
		super(message, cause);
	}

	public CryptoCompareException(String message) {
		super(message);
	}

	public CryptoCompareException(Throwable cause) {
		super(cause);
	}

	
	
}
